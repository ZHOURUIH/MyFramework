#if USE_OBFUZ
    // 设置Obfuz生成文件路径
    protected static bool setObfuzCodeOutputPath(string outputPath)
    {
        ObfuzSettings settings = ObfuzSettings.Instance;
        if (settings == null)
        {
            Debug.LogWarning("未找到ObfuzSettings,跳过Obfuz设置");
            return false;
        }
        settings.encryptionVMSettings ??= new EncryptionVMSettings();
        if (settings.encryptionVMSettings.codeOutputPath == outputPath)
        {
            return false;
        }
        settings.encryptionVMSettings.codeOutputPath = outputPath;
        EditorUtility.SetDirty(settings);
        return true;
    }
    // 设置Obfuz程序集配置:取消Obfuscate Obfuz Runtime,并添加热更程序集到Assemblies To Obfuscate
    protected static bool setObfuzAssemblySettings()
    {
        ObfuzSettings settings = ObfuzSettings.Instance;
        if (settings == null)
        {
            Debug.LogWarning("未找到ObfuzSettings,跳过Obfuz程序集设置");
            return false;
        }
        settings.assemblySettings ??= new AssemblySettings();
        bool changed = false;
        if (settings.assemblySettings.obfuscateObfuzRuntime)
        {
            settings.assemblySettings.obfuscateObfuzRuntime = false;
            changed = true;
        }

        string[] oldAssemblies = settings.assemblySettings.assembliesToObfuscate;
        string[] newAssemblies = addStringToArray(oldAssemblies, HOTFIX_FRAME, HOTFIX);
        if (newAssemblies != oldAssemblies)
        {
            settings.assemblySettings.assembliesToObfuscate = newAssemblies;
            changed = true;
        }
        if (changed)
        {
            EditorUtility.SetDirty(settings);
        }
        return changed;
    }
    // 向字符串数组中添加字符串,已存在则不重复添加
    protected static string[] addStringToArray(string[] array, params string[] values)
    {
        List<string> list = new();
        foreach (string item in array)
        {
            list.addUniqueIf(item, !item.isEmpty());
        }
        bool changed = false;
        foreach (string item in values)
        {
            if (list.addUniqueIf(item, !item.isEmpty()))
            {
                changed = true;
            }
        }
        return changed ? list.ToArray() : array;
    }
