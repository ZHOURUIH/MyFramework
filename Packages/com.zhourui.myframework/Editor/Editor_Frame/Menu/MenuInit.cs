    // 向AssemblyDefinitionAsset数组中添加asmdef,已存在则不重复添加
    protected static AssemblyDefinitionAsset[] addAsmdefToArray(AssemblyDefinitionAsset[] array, params string[] asmdefNames)
    {
        List<AssemblyDefinitionAsset> list = new();
        foreach (AssemblyDefinitionAsset item in array.safe())
        {
            list.addUniqueIf(item, item != null);
        }
        foreach (string name in asmdefNames)
        {
            AssemblyDefinitionAsset asmdef = findAsmdefByFileName(name);
            if (asmdef == null)
            {
                Debug.LogError("未找到asmdef:" + name);
                continue;
            }
            list.addUnique(asmdef);
        }
        return list.ToArray();
    }
    // 根据asmdef文件名查找AssemblyDefinitionAsset
    protected static AssemblyDefinitionAsset findAsmdefByFileName(string asmdefName)
    {
        foreach (string guid in AssetDatabase.FindAssets("t:AssemblyDefinitionAsset"))
        {
            string assetPath = AssetDatabase.GUIDToAssetPath(guid);
            if (Path.GetFileNameWithoutExtension(assetPath) == asmdefName)
            {
                return AssetDatabase.LoadAssetAtPath<AssemblyDefinitionAsset>(assetPath);
            }
        }
        return null;
    }
